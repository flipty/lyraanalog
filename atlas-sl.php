          <!--h3 class="my-3">Description</h3-->
            <p><img class="img-fluid" src="/images/ATLAS-LAMBDA-SL.jpg" style="width:60%; height:auto; padding: 0 25px 15px 0; float:left;" alt="Lyra Atlas SL &lambda;" title="Lyra Atlas SL &lambda;">The Atlas &lambda; is Lyra’s new flagship phono cartridge. Why asymmetric? By literally misplacing the “barriers” to great sound that are present in every other cartridge today, a number of important performance benefits are realized.</p>

			<p>First, different-shaped structures on the left and right sides of the cartridge body suppress the formation of standing waves within, resulting in a significant reduction of resonance-induced colorations. Second, the asymmetric construction offsets the front magnet carrier and its associated mounting system so that it is no longer in line with the cantilever assembly. This opens up a direct, solid path between the cantilever assembly and tonearm headshell so that vibrations from the cantilever can be quickly drained away once they have been converted into electrical signals, again suppressing induced resonances.</p>

			<p>Lyra remains the only cartridge manufacturer to mount the cantilever assembly directly into the cartridge body thereby achieving a seamless, totally rigid connection between the cantilever assembly and headshell. The effectiveness of this system has been enhanced as a result of the asymmetrical structure of the Atlas &lambda; Lambda. Control over spurious resonances is further assisted by the use of a narrowed mounting area, which couples the Atlas &lambda; more tightly to the headshell and facilitates the transfer of vibrational energy into the tonearm.</p>

			<p>The Atlas &lambda; Lambda retains the yokeless dual magnet system, diamond-coated boron rod cantilever and variable-radius line-contact stylus of the prior Atlas.</p>

			<p>The Atlas &lambda; Lambda uses <a href="http://lyraanalog.com/lyra-new-angle.php">Lyra’s “New Angle” technology</a>, which mechanically pre-biases the signal coils so that they are perfectly aligned to the front and rear magnets when LP playback takes place. This equalizes out discrepancies in vertical and horizontal compliances and enables the Atlas &lambda; Lambda coils to move with equal ease in all directions for optimal performance.</p>

			<p>A seldom-mentioned yet key factor in defining the performance and sound of a phono cartridge is the suspension and damper system. A cantilever’s materials, dimensions and constructions all affect the sound in unique ways, and unless the dampers and suspension are engineered to be a suitable match to the cantilever chosen, the sound and performance will not be optimal.</p>

			<p>For Lyra’s post-2008 New Angle models (Delos, Kleos, Etna, Atlas) we developed tapered dampers to pre-load the cantilever downwards when it is in the rest position, so that the application of vertical tracking force (VTF) will bring the coil angle into alignment with the magnets when the cartridge is in the playing position. Our continued research into dampers and suspension systems has now led to the next level up - Lambda.</p>

			<p>Lambda separates the tapered dampers of the New Angle cartridges into flat elastomer discs (for damping) and an additional support “pillow” to serve as the cantilever pre-loading element. This division allows the use of more specialized materials that are better suited to their individual tasks.</p>

			<p>The Lambda Atlas and Etna thereby continue the New Angle benefits (coil and magnet angles become aligned when the cartridge is in the playing position), but stability and sonic performance are significantly improved.</p>

			<p>The Lambda improvements apply to both the single coil layer models (Atlas SL, Etna SL) and the standard double coil layer models (Atlas, Etna). Likewise for the Atlas Mono and Etna Mono models.</p>

			<p>The Atlas &lambda; Lambda uses a body that is meticulously carved from a solid billet of titanium, through a process that involves both contact (for the exterior) and non-contact machining (for the interior body structures). By making most of the Atlas &lambda; Lambda body surfaces non-parallel (avoiding dimensions that are multiples of other dimensions) and adding a pre-stressed phase-interference resonance-controlling system, resonances have been further inhibited.</p>

			<p>We firmly believe that the Atlas λ Lambda represents an important step forward in LP playback.</p>

