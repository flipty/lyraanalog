            <p><img class="img-fluid" src="/images/ATLAS-ATLAS-SL-4x7.jpg" style="width:40%; height:auto; padding: 0 25px 15px 0; float:left;" alt="Lyra Atlas &lambda;" title="Lyra Atlas">At the 2012 Consumer Electronics Show, I spoke with Lyra”s Jonathan Carr about the Atlas &lambda;. He told me that, rather than having started as a blank sheet of paper, the Atlas &lambda; is an outgrowth of the Kleos ($2995), which I reviewed in January 2011, when I thought it Carr”s best balanced design yet, even if it didn't have quite the resolution of the Titan i. Like the lower-priced Delos ($1650, reviewed in August 2010), the Kleos included Carr's New Angle technology, which mechanically aligns the coils to be perfectly positioned relative to the front and rear magnets when the stylus is in the groove. This is said to equalize out any discrepancies in horizontal and vertical compliance so that the coils can move with equal ease in all directions. I would have thought that any properly designed cartridge would properly position the coils relative to the magnets during playback at the recommended tracking force, but maybe I'm missing something here.</p>

			<p>Carr said that, having been given a larger budget, he could experiment with a few engineering concepts he'd been considering, including using tuned resonators like the ones Finite Elemente uses in its equipment racks (and sells as discs for placing atop a component), to reduce resonance amplitude by converting it to heat.</p>

			<p>Carr says he found that, along with material, mass, and tuning frequency, where you placed these resonance killers greatly affected the sound. After he”d designed the Kleos, it struck Carr that the structure that secures the front magnet carrier to the cartridge body could be made to function as a resonance killer. However, the magnet carrier was perfectly centered on the cartridge body, and Carr had found that that central location was not an ideal place to put a resonance killer.</p>

			<p>So designing the Atlas &lambda; became, fundamentally, an exercise in how to move the front magnet carrier out of the position it occupied in the Kleos. Thus was born the idea for an asymmetrically designed cartridge whose motor retaining screw would no longer be in a center position, but somewhere that Carr's experience with resonance cancelers indicated it would be most beneficial.</p>

			<p>The second design goal was to create a more solid, direct path for vibrational energy to flow from the cantilever to the headshell. Carr felt that putting a screw hole between cantilever and headshell would obstruct that path, so he moved the screw hole away from the vertical central line.</p>

			<p>Carr also said that, in the Atlas &lambda;, he tried to avoid using dimensions that were even multiples of other dimensions. Instead of using 2, 4, 6, 8, he used 3, 5, 7, 11, 13. If you look at the section of the body directly behind the cantilever of a normal cartridge, he said, the walls are parallel; in the Atlas &lambda;, the walls form a V. Again, the goal was to avoid having parallel surfaces at any critical point in the cartridge. “The entire cartridge,” he told me, “consists of curves balanced against angles, nonparallel surfaces, nonmultiple dimensions.”</p>

			<p>“So why do people make cartridge bodies out of wood?” I asked.</p>

			<p>“Probably because they like the resonant character that wood imparts,” he replied. “If they enjoy it, great for them. Whatever makes them happy.” His tone was not sarcastic.</p>

			<p>Carr told me that while the Atlas”s cantilever of diamond-coated boron is similar to the one used in the Titan i, and while the styli (a variable-radius, line-contact, nude diamond measuring 3 by 70µm) are identical, the Atlas &lambda; mounting structure is stiffer than the Titan”s, and the coils are completely different. Instead of a square, the coils form an X, which he says produces better channel separation and tracking. The magnet former is chemically purified iron.</p>

			<p>The Atlas &lambda; retains Lyra's yokeless dual-magnet system, and a unique construction that integrates the cantilever assembly into the cartridge body rather than simply installing a complete, standalone motor assembly inside a body. And, like the bodies of the Olympos and Titan i, the Atlas &lambda; is machined from a solid billet of titanium.</p>

			<p>The Atlas &lambda; motor is more efficient; its output voltage is 12% higher than the Titan's, while the amount of 99.9999%-pure copper coil wire has been reduced by 22%, reducing the moving mass. In my opinion, that”s as significant as any other improvement Carr mentioned. The cartridge outputs 0.56mV/5cm/s (using the CBS Test Record).</p>

			<p>The Lyra Atlas &lambda; costs $9500 [gulp]. If you're Php Aide someone who looks at such a product, adds up its parts costs, and concludes that it's overpriced, please consider what Carr told me: He devoted every working moment of the past year to its design. I'm just thrilled that anyone devotes this sort of time and attention to designing and making phono cartridges. I'm talking about not only Jonathan Carr of Lyra, but of Leif Johannsen of Ortofon, the Suchys of Clearaudio, Peter Ledermann of Soundsmith, and Matsudaira-san of My Sonic Lab. The fact that I could list even more is, in 2012, almost freakily amazing, don”t you think?</p>

			<h2>The Sound of the Atlas &lambda;</h2>

			<p>Out of the box, mounted in the Kuzma 4Point tonearm on the Continuum Caliburn turntable, before it had even a chance to break in (but after I'd sorted out the SRA problem), the Atlas &lambda; transparency and tonal neutrality were immediately evident. Jonathan Carr has managed safest online casino to combine the Titan i's unsurpassed retrieval of detail, and transient speed and purity, with the Kleos's well-balanced, velvety warmth and inviting smoothness.</p>

			<p>While I never had an Olympos at home, I've heard it in familiar systems, and have always found it smoother and more polite on top than I like—though I can hear why some might prefer it to the Titan's more revealing, more analytical top end. I'd say the Atlas &lambda; splits the difference, but that would be selling it short. Tonally, it opens a window on the music, much as the Ortofon A90 does, but it's dynamically superior, and the most dynamically revealing—particularly when it comes to microdynamics—that I've yet heard.</p>

			<p>I played Analogue Productions' reissue of Norah Jones's first album, Come Away With Me, and the speed with which the Atlas &lambda; reacted to small changes in voice level, previously buried low-level inflections jumping from the speakers, made these very familiar performances new again—and I pulled out an original pressing to be sure it wasn”t just the remastering.</p>

			<p>Like the Ortofon A90, the Lyra Atlas &lambda; transmitted and released energy with alarming speed, leaving no residue to rattle around, repeat, cloud, or confuse the next musical instant. Images just “popped” in space. Bass, taut and nimble, dug all the way down—but only when it was engraved in the grooves to begin with.</p>

			<p>The Atlas &lambda; overall sound was positively effortless. It carved images precisely, without the sharp edges that the Titan's critics accuse it of leaving. Vocal sibilants were clean and smooth, yet precise and sharp when they should have been.</p>

			<p>I'd installed the Titan in the Kuzma 4Point tonearm, so I could go back and forth between it and the Atlas &lambda;. The Atlas &lambda; managed to be both far faster and more revealing than the Titan, as well as smoother and more detailed and more transparent. The Atlas &lambda; imaging was more precise, and more finely rendered in sharper relief on an even blacker stage—and believe me, the Titan itself is no slouch in those departments.</p>

			<p>Harmonically, there was no contest. It was sort of like the difference between Lyra's Helikon and Kleos: the Atlas &lambda; produced a greater profusion of harmonic riches, leaving the very good Titan sounding a bit drab by comparison. Helping in that determination was a nifty, revealing record, The Instruments of the Orchestra, arranged, presented, and conducted by Sir Malcolm Sargent (LP, Decca Eclipse ECS 2102), as well as the always stupendous Royal Ballet: Gala Performances (RCA Living Stereo/Classic LDS-6065).</p>

			<p>The Atlas &lambda; is superior to the Titan in every way, and by not-small amounts—as it should be, given the differences in technological complexity and price. If I had to pick one parameter that most impressed me, it would be the Atlas &lambda; almost unnerving transparency, and its ability to create an utter “blackness” that I could almost see behind the images it carved in perfect relief.</p>

			<p>I haven't mentioned soundstage width and depth, etc., but those go without saying. You might find this difficult to believe if you own a Titan, but compared to the Atlas &lambda;, it sounded positively opaque overall, and its transient response was smoothed over—though it's possible that the many hours of play I've put on my Titan have diminished its ability to trace high frequencies.</p>

			<h2>Conclusion</h2>

			<p>The Lyra Atlas &lambda; is a complete success. All of the work Jonathan Carr has put in to diminish or eliminate resonances in the cartridge body, among other things, has paid off. If you can afford an Atlas &lambda;, you won't regret buying one, even if you've been leery of Lyra's reputation—undeserved, in my opinion—for being overly analytical, and even if you listen almost exclusively to classical music. This past weekend I played Paavo Järvi and the Deutsche Kammerphilharmonie Bremen's entire cycle of Beethoven symphonies, and it didn't suck!</p>

