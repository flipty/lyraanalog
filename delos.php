                          While vinyl records played back with an MC cartridge can sound exceptionally good, it is a surprising fact that the design of conventional MCs restricts the sound quality that can be achieved from LPs.</p>

      <p>An essential factor for proper operation of an MC cartridge is the angle between the magnetic circuit, signal coils and core. Performance is optimal when the angles are all aligned, but any significant difference in the angles will cause the formation of a strong and directional flux that will flow constantly from the magnetic circuit into the signal core. This directional magnetic flux will orient the core toward a specific direction and make it impossible for the core to move with equal ease in all directions, which is necessary for proper cartridge performance.</p>

      <p>Unfortunately, conventional MC cartridges have been designed so that the angles between magnetic circuit, signal coils and core become aligned only when tracking force is not applied. The result is that during playback, the required tracking force causes the angle between magnetic circuit, signal coils and core to become misaligned. This limits the cartridge’s resolution, tracking ability and dynamic range.</p>

      <p>The new Lyra Delos is designed to conquer this problem. Compared to conventional cartridges, the Delos has a new body structure with an unusually shallow angle, and this has been paired with a unique asymmetrical damping system. When no tracking force is applied, the shape of the asymmetrical dampers forces the signal coils and core into a more upright angle than the magnetic circuit.</p>

      <p>However, no tracking force applied means that the cartridge is not operating, and therefore any angular discrepancies between magnetic circuit, signal coils and core will have no effect on performance. When tracking force is applied, however, the force of the stylus pushing on the LP causes the asymmetrical dampers to be deformed into a symmetrical shape, and when this happens the angular discrepancies between magnetic circuit, signal coils and core all disappear. Since tracking force applied means that the cartridge is now operating, it should be clear that the design of the Delos is able to keep the angles between the magnetic circuit, signal coils and core aligned during playback.</p>

      <p>Keeping the proper angular alignment prevents the formation of any strong and directional flux that could otherwise flow constantly from the magnetic circuit into the signal core, and enables the Delos’ core to move with equal ease in all directions, for optimal performance.</p>

      <p>The Delos also has a microridge stylus on a boron cantilever for outstanding tracking, a non-parallel solid machined metal body for low resonances, and 6-N high-purity copper coils and nude construction for greater clarity. The result is clearly improved sound quality, particularly resolution, tracking ability, dynamic range, transient impact and immediacy.</p>

