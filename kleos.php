              <p><img class="img-fluid" src="/images/KLEOS-MONO-4x7.jpg" style="width:60%; height:auto; padding: 0 25px 15px 0; float:left;" alt="Lyra Kleos Mono" title="Lyra Kleos Mono">This is a special, built-to-order, mono version of Kleos for real mono playback of monaural/monophonic (mono) microgroove, vinyl records. It is recommended that the Kleos Mono is used with high-gain, low noise phono stages, or alternatively a step-up transformer designed for phono cartridges of 2 - 3 ohms or less.</p>
              <p>On mono records, the vertical axis of the groove contains no musical information, but it will frequently have noise, in the form of groove damage and dirt. The Kleos Mono has therefore been designed to be completely insensitive to the vertical axis, which greatly improves the signal/noise ratio without any downside on monaural records.</p>
              <p>The coil former is a square permeable plate oriented parallel to the record surface rather than the 45-degree angle used for most stereo cartridges, while the coils are wound so that they only generate a signal when there is horizontal movement of the stylus and cantilever when tracking a record.</p>
              <p>Two totally separate monaural coils are used in the Kleos Mono, since most monaural cartridges are used with stereo amplifiers and stereo speakers. The separate coils also help avoid possible ground loops and hum problems that could otherwise be caused if a single coil is fed into a stereo two-channel amplification system, thereby tying the two channels together electrically.</p>
              <p>In addition to parallel mono connection to a stereo system (two amplifiers and two speakers), it is also possible to leave one set of connections unattached for pure mono playback with a single amplifier and a single speaker. However, it is also possible to connect the two mono channels in series for additional output when used in a pure mono system. Use a short cartridge headshell lead wire with female pin-connectors to connect either the Kleos Mono's upper right pin with the lower left pin, or alternatively the upper left pin with the lower right pin. Then use the two remaining unused pins for connection to your single amp RIAA input (alternatively a transformer input). The two internal channels are now "chained" in serial-connection for additional output and energy when you only use a single amplifier and a single speaker.</p>
