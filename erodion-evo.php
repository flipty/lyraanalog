              Lyra’s Erodion EVO is a Moving Coil Step-Up Interface (MC step-up transformer) especially designed for Lyra cartridges, but also a terrific fit with any low-impedance cartridge of 2 - 10 ohms.</p>


<p>Erodion EVO is a particularly compelling mate to Lyra’s SL (Single-Layer coil) MC cartridges - Kleos SL, Etna Lambda SL and Atlas Lambda SL. 
With less noise and, and more importantly, fewer electronic artifacts than most phono stages, many audiophiles will find improved performance from Erodion EVO, even when it is paired with well-respected high-gain, low-noise phono stages.</p>

<p>Additionally, Erodion EVO has wider bandwidth and dynamic range than other step-up transformers, which results in sonic impact that is between an active head amp and a conventional step-up transformer.
Regardless of the gain potential of a given phono stage, Erodion EVO delivers an utterly addictive and punchy musicality to Lyra’s SL cartridges that may be unrivalled. </p>

<p>With 26dB gain (20x) the Erodion EVO increases the signal voltage and output impedance of low-output, low-impedance MC cartridges up to a level that's comfortable for MM-compatible phono inputs of preamplifiers and phono stages that have input loads of around 47 kohms.</p>

<p>Erodion EVO includes an integrated Lyra-designed-and-made low-capacitance PhonoPipe output cable (60cm or approx. 2ft.) for connection to your phono stage. There are two switchable grounding positions. Position 2 (floating ground) is recommended.</p>

<h3 class="my-3">Specifications for Erodion EVO</h3>
<ul>
<li> <strong>Gain:</strong> 26 dB (20 times) (when cartridge's internal impedance is 2 - 10 ohms and MM level RIAA preamplifier or phono stage  presents 47 kohms load)</li>
