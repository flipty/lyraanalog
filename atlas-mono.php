          <!--h3 class="my-3">Description</h3-->
            <p><img class="img-fluid" src="/images/ATLAS-LAMBDA-MONO.jpg" style="width:60%; height:auto; padding: 0 25px 15px 0; float:left;" alt="Lyra Atlas Mono" title="Lyra Atlas Mono">This is a special, built-to-order, mono version of Atlas for real mono playback of monaural/monophonic (mono) microgroove, vinyl records. It is recommended that the Atlas Mono is used with high-gain, low noise phono stages, or alternatively a step-up transformer designed for phono cartridges of 2 - 3 ohms or less.</p>

			<p>On mono records, the vertical axis of the groove contains no musical information, but it will frequently have noise, in the form of groove damage and dirt. The Atlas Mono has therefore been designed to be completely insensitive to the vertical axis, which greatly improves the signal/noise ratio without any downside on monaural records.</p>

			<p>The coil former is a square permeable plate oriented parallel to the record surface rather than the 45-degree angle used for most stereo cartridges, while the coils are wound so that they only generate a signal when there is horizontal movement of the stylus and cantilever when tracking a record.</p>

			<p>Two totally separate monaural coils are used in the Atlas Mono, since most monaural cartridges are used with stereo amplifiers and stereo speakers. The separate coils also help avoid possible ground loops and hum problems that could otherwise be caused if a single coil is fed into a stereo two-channel amplification system, thereby tying the two channels together electrically.</p>

			<p>In addition to parallel mono connection to a stereo system (two amplifiers and two speakers), it is also possible to leave one set of connections unattached for pure mono playback with a single amplifier and a single speaker. However, it is also possible to connect the two mono channels in series for additional output when used in a pure mono system. Use a short cartridge headshell lead wire with female pin-connectors to connect either the Atlas Mono's upper right pin with the lower left pin, or alternatively the upper left pin with the lower right pin. Then use the two remaining unused pins for connection to your single amp RIAA input (alternatively a transformer input). The two internal channels are now "chained" in serial-connection for additional output and energy when you only use a single amplifier and a single speaker.</p>

			<p>The Atlas &lambda; Lambda Mono uses <a href="http://lyraanalog.com/lyra-new-angle.php">Lyra’s “New Angle” technology</a>, which mechanically pre-biases the signal coils so that they are perfectly aligned to the front and rear magnets when LP playback takes place. This equalizes out discrepancies in vertical and horizontal compliances and enables the Atlas λ Lambda Mono coils to move with equal ease in all directions for optimal performance.</p>

			<p>A seldom-mentioned yet key factor in defining the performance and sound of a phono cartridge is the suspension and damper system. A cantilever’s materials, dimensions and constructions all affect the sound in unique ways, and unless the dampers and suspension are engineered to be a suitable match to the cantilever chosen, the sound and performance will not be optimal.</p>

			<p>For Lyra’s post-2008 New Angle models (Delos, Kleos, Etna, Atlas) we developed tapered dampers to pre-load the cantilever downwards when it is in the rest position, so that the application of vertical tracking force (VTF) will bring the coil angle into alignment with the magnets when the cartridge is in the playing position. Our continued research into dampers and suspension systems has now led to the next level up - Lambda.</p>

			<p>Lambda separates the tapered dampers of the New Angle cartridges into flat elastomer discs (for damping) and an additional support “pillow” to serve as the cantilever pre-loading element. This division allows the use of more specialized materials that are better suited to their individual tasks.</p>

			<p>The Lambda Atlas and Etna thereby continue the New Angle benefits (coil and magnet angles become aligned when the cartridge is in the playing position), but stability and sonic performance are significantly improved.</p>

			<p>The Lambda improvements apply to both the single coil layer models (Atlas SL, Etna SL) and the standard double coil layer models (Atlas, Etna). Likewise for the Atlas Mono and Etna Mono models.</p>

			<p>The Atlas &lambda; Lambda Mono uses a body that is meticulously carved from a solid billet of titanium, through a process that involves both contact (for the exterior) and non-contact machining (for the interior body structures). By making most of the Atlas &lambda; Lambda body surfaces non-parallel (avoiding dimensions that are multiples of other dimensions) and adding a pre-stressed phase-interference resonance-controlling system, resonances have been further inhibited.</p>

